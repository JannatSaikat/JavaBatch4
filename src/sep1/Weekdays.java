package sep1;

public class Weekdays {

    public static void main(String[] args) {
        String day = "Sun";

        if(day == "Sat") {
            System.out.println("Weekend");
        } else if( day == "Sun") {
            System.out.println("Weekend");
        } else if(day == "Mon") {
            System.out.println("Weekday");
        } else if(day == "Tue") {
            System.out.println("Weekday");
        } else if(day == "Wed") {
            System.out.println("Weekday");
        } else if(day == "Thu") {
            System.out.println("Weekday");
        } else if(day == "Fri") {
            System.out.println("Weekday");
        } else {
            System.out.println("Wrong input");
        }
    }
}
