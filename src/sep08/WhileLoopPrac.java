package sep08;

public class WhileLoopPrac {

    public static void main(String[] args) {

        int a = 5;

        if(a < 10) {
            System.out.println( a + " is less than 10");
        }

        System.out.println("=====================");

       while (a < 10) {
           System.out.println( a + " is less than 10");
           a--;
       }



    }
}
