package sep15;

public class WhileRecap {

    public static void main(String[] args) {
        // starting/ initialization, logic , exiting
        //Print numbers from -10 to -1 using while loop

        int a = -10; //initialization

        while (a < 0) { //logic
            System.out.println(a);
            a++;   //increment/decrement
        }

        System.out.println("----------------");
      //  for(initialization ; logic ; increment/decrement)

        for(int b = -10; b <= -1; b++) {
            System.out.println(a);
        }



    }
}
